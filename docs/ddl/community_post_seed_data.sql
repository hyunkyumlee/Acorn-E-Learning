  seed.board_type, seed.title, seed.content, seed.view_count, seed.like_count, seed.comment_count, seed.scrap_count,
  'ACTIVE', DATE_SUB(CURRENT_TIMESTAMP, INTERVAL seed.days_ago DAY), CURRENT_TIMESTAMP, NULL
FROM tmp_realistic_community_posts seed;

INSERT INTO comments (post_id, parent_comment_id, writer_id, content, status, created_at, updated_at, deleted_at, deleted_by_admin_id)
SELECT
  post.post_id, NULL,
  CASE comment_seed.writer_no WHEN 1 THEN @writer_1 WHEN 2 THEN @writer_2 WHEN 3 THEN @writer_3 WHEN 4 THEN @writer_4 ELSE @writer_5 END,
  comment_seed.content, 'ACTIVE', DATE_SUB(CURRENT_TIMESTAMP, INTERVAL comment_seed.days_ago DAY), CURRENT_TIMESTAMP, NULL, NULL
FROM tmp_realistic_community_comments comment_seed
JOIN tmp_realistic_community_posts post_seed ON post_seed.seed_key = comment_seed.seed_key
JOIN subjects subject ON subject.subject_code = post_seed.subject_code
JOIN community_posts post ON post.subject_id = subject.subject_id AND post.title = post_seed.title AND post.status = 'ACTIVE';

UPDATE community_posts post
JOIN tmp_realistic_community_posts seed ON seed.title = post.title
JOIN subjects subject ON subject.subject_id = post.subject_id AND subject.subject_code = seed.subject_code
SET post.comment_count = (
  SELECT COUNT(*) FROM comments c WHERE c.post_id = post.post_id AND c.status = 'ACTIVE'
)
WHERE post.status = 'ACTIVE';

